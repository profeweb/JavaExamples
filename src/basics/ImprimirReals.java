package basics;

public class ImprimirReals {
    public static void main(String[] args) {

        double a = 7.75;      // real-form & double type
        double b = 3.14159d;  // real-form & double type
        double c = .45e-5;    // real-form & double type
        float d = -5.025f;    // real-form & float type
        float e = 123.45f;    // real-form & float type

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}